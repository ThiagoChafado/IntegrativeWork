
import "../CurrentSeller/styleEdit.css";
import React from "react";
import './calcStyle.css'

import { useNavigate, useParams } from "react-router-dom";
import axios from "axios";
import defaultMonth from "../Controllers/defaultMonth";
axios.defaults.baseURL = "http://localhost:3001";
function Calculator() {
  const [selectedMonth, setSelectedMonth] = React.useState();
  const [sellerList, setSellerList] = React.useState();
  const aux = useParams();
  const shopname = aux.shopname;
  const navigate = useNavigate();

  React.useEffect(() => {
    if (!localStorage.getItem("tokensuper")) {
      navigate("/loginsuper");
    }
  }, [navigate]);


  React.useEffect(() => {
    getDefaultMonth();
  }, []);

  React.useEffect(() => {
    if (selectedMonth != undefined) {
      getPcc();
    }
  }, [selectedMonth]);

  async function getPcc() {
    const res = await axios.get(
      "/sellers/sellersells/" + shopname + "/" + selectedMonth
    );
    setSellerList(res.data);
  }

  function getDefaultMonth() {
    const pcMonth = defaultMonth();
    setSelectedMonth(pcMonth);
  }

  return (
    <div className="mainCalc">
        <div className="boxCalc">
            <h1>Informe a Data</h1>
            <input
              type="month"
              id="monthInput"
              name="monthInput"
              value={selectedMonth}
              onChange={(e) => setSelectedMonth(e.currentTarget.value)}
            ></input>
            {sellerList &&
              sellerList.map((i) => {
                const calculate = ((i.pccommision/100 ) * i.sum).toFixed(2);
                return (
                  <div className="sellerD">
                  <p>
                  <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-clipboard-data" viewBox="0 0 16 16">
  <path d="M4 11a1 1 0 1 1 2 0v1a1 1 0 1 1-2 0zm6-4a1 1 0 1 1 2 0v5a1 1 0 1 1-2 0zM7 9a1 1 0 0 1 2 0v3a1 1 0 1 1-2 0z"/>
  <path d="M4 1.5H3a2 2 0 0 0-2 2V14a2 2 0 0 0 2 2h10a2 2 0 0 0 2-2V3.5a2 2 0 0 0-2-2h-1v1h1a1 1 0 0 1 1 1V14a1 1 0 0 1-1 1H3a1 1 0 0 1-1-1V3.5a1 1 0 0 1 1-1h1z"/>
  <path d="M9.5 1a.5.5 0 0 1 .5.5v1a.5.5 0 0 1-.5.5h-3a.5.5 0 0 1-.5-.5v-1a.5.5 0 0 1 .5-.5zm-3-1A1.5 1.5 0 0 0 5 1.5v1A1.5 1.5 0 0 0 6.5 4h3A1.5 1.5 0 0 0 11 2.5v-1A1.5 1.5 0 0 0 9.5 0z"/>
</svg> Vendedor: {i.sellername} <br></br> 
                  </p>


<p> <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-clipboard-data" viewBox="0 0 16 16">
  <path d="M4 11a1 1 0 1 1 2 0v1a1 1 0 1 1-2 0zm6-4a1 1 0 1 1 2 0v5a1 1 0 1 1-2 0zM7 9a1 1 0 0 1 2 0v3a1 1 0 1 1-2 0z"/>
  <path d="M4 1.5H3a2 2 0 0 0-2 2V14a2 2 0 0 0 2 2h10a2 2 0 0 0 2-2V3.5a2 2 0 0 0-2-2h-1v1h1a1 1 0 0 1 1 1V14a1 1 0 0 1-1 1H3a1 1 0 0 1-1-1V3.5a1 1 0 0 1 1-1h1z"/>
  <path d="M9.5 1a.5.5 0 0 1 .5.5v1a.5.5 0 0 1-.5.5h-3a.5.5 0 0 1-.5-.5v-1a.5.5 0 0 1 .5-.5zm-3-1A1.5 1.5 0 0 0 5 1.5v1A1.5 1.5 0 0 0 6.5 4h3A1.5 1.5 0 0 0 11 2.5v-1A1.5 1.5 0 0 0 9.5 0z"/>
</svg> Total No Mês: R${i.sum}</p>
                      
<p>
<svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-clipboard-data" viewBox="0 0 16 16">
  <path d="M4 11a1 1 0 1 1 2 0v1a1 1 0 1 1-2 0zm6-4a1 1 0 1 1 2 0v5a1 1 0 1 1-2 0zM7 9a1 1 0 0 1 2 0v3a1 1 0 1 1-2 0z"/>
  <path d="M4 1.5H3a2 2 0 0 0-2 2V14a2 2 0 0 0 2 2h10a2 2 0 0 0 2-2V3.5a2 2 0 0 0-2-2h-1v1h1a1 1 0 0 1 1 1V14a1 1 0 0 1-1 1H3a1 1 0 0 1-1-1V3.5a1 1 0 0 1 1-1h1z"/>
  <path d="M9.5 1a.5.5 0 0 1 .5.5v1a.5.5 0 0 1-.5.5h-3a.5.5 0 0 1-.5-.5v-1a.5.5 0 0 1 .5-.5zm-3-1A1.5 1.5 0 0 0 5 1.5v1A1.5 1.5 0 0 0 6.5 4h3A1.5 1.5 0 0 0 11 2.5v-1A1.5 1.5 0 0 0 9.5 0z"/>
</svg>Comissão: R${calculate}
</p>
                  
                  </div>
                );
              })}
      </div>
    </div>
  );
}
export default Calculator;
